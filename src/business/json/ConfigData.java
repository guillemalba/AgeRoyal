package business.json;

public class ConfigData {
    private int port;
    private String user;
    private String password;
    private String db;
    private String url;

    public ConfigData(){}

    public int getPort() {
        return port;
    }
    public String getUser() {
        return user;
    }
    public String getPassword() {
        return password;
    }
    public String getDb() {
        return db;
    }

    public String getUrl() {
        return url;
    }
}
